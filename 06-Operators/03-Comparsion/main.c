#include <stdio.h>
#include <stdlib.h>

int main(){

    // Comparison operators
    int a = 10, b = 20;
    printf("a: %d, b: %d\n", a, b);
    printf("a == b: %d\n", a == b); // Equal to
    printf("a != b: %d\n", a != b); // Not equal to
    printf("a > b: %d\n", a > b);   // Greater than
    printf("a < b: %d\n", a < b);   // Less than

    return 0;
}